    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        
        salvarAlterar = "Alterar";
        habilitarDesabilitarCampos(true);
        int linha = jTableProduto.getSelectedRow();
        
        try {
            
            int codigoProduto = (int) jTableProduto.getValueAt(linha, 0);
            
            //RECUPERAR DADOS DO BANCO
            modelProdutos = controllerProdutos.retornarUmProdutoController(codigoProduto);
            
            //SETAR NA INTERFACE
            this.jtfCodigo.setText(String.valueOf(modelProdutos.getIdProduto()));
            this.jtfNome.setText(String.valueOf(modelProdutos.getProNome()));
            this.jtfValorEntrada.setText(String.valueOf(modelProdutos.getProValorEntrada()));
            this.jtfValorSaida.setText(String.valueOf(modelProdutos.getProValorSaida()));
            this.jtfEstoque.setText(String.valueOf(modelProdutos.getProEstoque()));
            this.jtfValidade.setText(String.valueOf(modelProdutos.getProValidade()));
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "O registro não foi selecionado!", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jbAlterarActionPerformed


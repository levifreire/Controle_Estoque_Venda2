    /**
     * CARREGA A TABLE PRODUTOS COM OS PRODUTOS DO BANCO
     */
    private void carregarProdutos(){
        listaModelProduto = controllerProdutos.listarProdutosController();
        DefaultTableModel modelo = (DefaultTableModel) jTableProduto.getModel();
        modelo.setNumRows(0);
        
        //Setando produtos na tabela
        int cont = listaModelProduto.size();
        
        for (int i = 0; i < cont; i++) {
            //Adicionando um linha com as seguintes informações
            modelo.addRow(new Object[]{
                listaModelProduto.get(i).getIdProduto(),
                listaModelProduto.get(i).getProNome(),
                listaModelProduto.get(i).getProValorEntrada(),
                listaModelProduto.get(i).getProValorSaida(),
                listaModelProduto.get(i).getProEstoque(),
                listaModelProduto.get(i).getProValidade()
            });
        }
        
    }
    
    /**
     * HABILITAR E DESABILITAR CAMPOS E BOTÕES
     * @param condicao 
     */
    private void habilitarDesabilitarCampos(boolean condicao){
        //CAMPOS
        jtfNome.setEnabled(condicao);
        jtfValorEntrada.setEnabled(condicao);
        jtfValorSaida.setEnabled(condicao);
        jtfEstoque.setEnabled(condicao);
        jtfValidade.setEnabled(condicao);
        
        //BOTÕES
        jbAlterar.setEnabled(condicao);
        jbCancelar.setEnabled(condicao);
        jbExcluir.setEnabled(condicao);
        jbSalvar.setEnabled(condicao);
        
        //TABELA
        jTableProduto.setEnabled(condicao);
        
    }
    
    //LIMPAR CAMPOS DO FORMULARIO
    private void limparCampos(){
        
        jtfNome.setText("");
        jtfPesquisar.setText("");
        jtfValidade.setText("");
        jtfEstoque.setText("");
        jtfValorEntrada.setText("");
        jtfValorSaida.setText("");
        
    }
    
    private void salvarProduto(){
        
        //SALVANDO PRODUTOS NO BANCO
        modelProdutos.setProNome(this.jtfNome.getText());
        modelProdutos.setProValorEntrada(formatador.converterVirgulaParaPonto(this.jtfValorEntrada.getText()));
        modelProdutos.setProValorSaida(formatador.converterVirgulaParaPonto(this.jtfValorSaida.getText()));
        modelProdutos.setProValidade(Date.valueOf(this.jtfValidade.getText()));
        modelProdutos.setProEstoque(Integer.parseInt(this.jtfEstoque.getText()));
        
        if(controllerProdutos.salvarProdutoController(modelProdutos) > 0){
            JOptionPane.showMessageDialog(this, "Produto Salvo com sucesso!");
            carregarProdutos();
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "ERRO ao tentar salvar!");
        }
        
    }
    
    //METODO PARA ALTERAR UM PRODUTO
    private void alterarProduto(){
        
        //ALTERANDO PRODUTOS NO BANCO
        modelProdutos.setProNome(this.jtfNome.getText());
        modelProdutos.setProValorEntrada(formatador.converterVirgulaParaPonto(this.jtfValorEntrada.getText()));
        modelProdutos.setProValorSaida(formatador.converterVirgulaParaPonto(this.jtfValorSaida.getText()));
        modelProdutos.setProValidade(Date.valueOf(this.jtfValidade.getText()));
        modelProdutos.setProEstoque(Integer.parseInt(this.jtfEstoque.getText()));
        
        if(controllerProdutos.alterarProdutoController(modelProdutos)){
            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
            carregarProdutos();
            habilitarDesabilitarCampos(false);
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(this, "ERRO ao tentar alterar", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    
    private void pesquisarProduto(){
        
        DefaultTableModel modelo = (DefaultTableModel) this.jTableProduto.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.jTableProduto.setRowSorter(classificador);
        String texto = jtfPesquisar.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
        
        if(this.jtfPesquisar.getText().equals("")){
            carregarProdutos();
        }
        
    }
    
    

